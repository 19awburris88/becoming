export const getDaysUntil = (dateStr) => {
  const target = dayjs(dateStr)
  const today = dayjs()
  const thisYear = target.year(today.year())
  if (thisYear.isBefore(today, 'day')) {
    return thisYear.add(1, 'year').diff(today, 'day')
  }
  return thisYear.diff(today, 'day')
}

export const getUpcomingDates = () => {
  const upcoming = []
  importantDates.forEach((d) => {
    const daysUntil = getDaysUntil(d.date)
    if (daysUntil >= 0 && daysUntil <= 60) {
      upcoming.push({ ...d, daysUntil })
    }
  })
  return upcoming.sort((a, b) => a.daysUntil - b.daysUntil)
}

